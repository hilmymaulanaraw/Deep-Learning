def map_images_to_metadata(df, base_path):
    # Extract all possible UIDs from the 'image file path' column
    # The format is 'Project_Patient_Laterality_View/UID1/UID2/000000.dcm'
    def get_potential_uids(p):
        if isinstance(p, str):
            return set(p.split('/'))
        return set()

    # Map every UID folder in the jpeg directory to its actual .jpg file path
    jpeg_dir = os.path.join(base_path, 'jpeg')
    uid_map = {}
    for root, dirs, files in os.walk(jpeg_dir):
        for f in files:
            if f.endswith('.jpg'):
                # Use the immediate parent folder name as the key (UID)
                uid_map[os.path.basename(root)] = os.path.join(root, f)

    def match_path(p):
        uids = get_potential_uids(p)
        for uid in uids:
            if uid in uid_map:
                return uid_map[uid]
        return None

    df = df.copy()
    df['full_path'] = df['image file path'].apply(match_path)
    df['label'] = df['pathology'].apply(lambda x: 1 if 'MALIGNANT' in str(x) else 0)

    cleaned_df = df.dropna(subset=['full_path']).copy()
    return cleaned_df

