def remove_artifacts_and_extract_roi(image):
    # 1. Thresholding to create a mask of the breast
    gray = cv2.cvtColor(np.uint8(image * 255), cv2.COLOR_RGB2GRAY)
    _, thresh = cv2.threshold(gray, 10, 255, cv2.THRESH_BINARY)

    # 2. Find the largest contour (the breast)
    contours, _ = cv2.findContours(thresh, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    if not contours:
        return image

    largest_cnt = max(contours, key=cv2.contourArea)
    mask = np.zeros_like(gray)
    cv2.drawContours(mask, [largest_cnt], -1, 255, -1)

    # 3. Apply mask
    result = cv2.bitwise_and(np.uint8(image * 255), np.uint8(image * 255), mask=mask)

    # 4. Crop to bounding box
    x, y, w, h = cv2.boundingRect(largest_cnt)
    roi = result[y:y+h, x:x+w]

    # Resize back to target size
    roi_resized = cv2.resize(roi, (224, 224))
    return np.float32(roi_resized) / 255.0

