"""### 10. Implementasi Arsitektur Attention U-Net"""

from tensorflow.keras import layers, models, backend as K
import tensorflow as tf

def attention_block(x, gating, inter_shape):
    shape_x = K.int_shape(x)
    phi_g = layers.Conv2D(inter_shape, (1, 1), padding='same')(gating)
    theta_x = layers.Conv2D(inter_shape, (1, 1), strides=(1, 1), padding='same')(x)
    concat_xg = layers.add([theta_x, phi_g])
    act_xg = layers.Activation('relu')(concat_xg)
    psi_xg = layers.Conv2D(1, (1, 1), padding='same')(act_xg)
    sigmoid_xg = layers.Activation('sigmoid')(psi_xg)
    y = layers.multiply([sigmoid_xg, x])
    result = layers.Conv2D(shape_x[3], (1, 1), padding='same')(y)
    result_bn = layers.BatchNormalization()(result)
    return result_bn

def conv_block(x, filters):
    x = layers.Conv2D(filters, (3, 3), padding="same")(x)
    x = layers.BatchNormalization()(x)
    x = layers.Activation("relu")(x)
    x = layers.Conv2D(filters, (3, 3), padding="same")(x)
    x = layers.BatchNormalization()(x)
    x = layers.Activation("relu")(x)
    return x

def build_attention_unet_classifier(input_shape=(224, 224, 3)):
    inputs = layers.Input(input_shape)

    # Encoder
    c1 = conv_block(inputs, 64)
    p1 = layers.MaxPooling2D((2, 2))(c1)
    c2 = conv_block(p1, 128)
    p2 = layers.MaxPooling2D((2, 2))(c2)
    c3 = conv_block(p2, 256)
    p3 = layers.MaxPooling2D((2, 2))(c3)
    c4 = conv_block(p3, 512)
    p4 = layers.MaxPooling2D((2, 2))(c4)

    # Bridge
    b1 = conv_block(p4, 1024)

    # Decoder
    u1 = layers.Conv2DTranspose(512, (2, 2), strides=(2, 2), padding='same')(b1)
    att1 = attention_block(x=c4, gating=u1, inter_shape=512)
    c5 = conv_block(layers.concatenate([u1, att1]), 512)

    u2 = layers.Conv2DTranspose(256, (2, 2), strides=(2, 2), padding='same')(c5)
    att2 = attention_block(x=c3, gating=u2, inter_shape=256)
    c6 = conv_block(layers.concatenate([u2, att2]), 256)

    u3 = layers.Conv2DTranspose(128, (2, 2), strides=(2, 2), padding='same')(c6)
    att3 = attention_block(x=c2, gating=u3, inter_shape=128)
    c7 = conv_block(layers.concatenate([u3, att3]), 128)

    u4 = layers.Conv2DTranspose(64, (2, 2), strides=(2, 2), padding='same')(c7)
    att4 = attention_block(x=c1, gating=u4, inter_shape=64)
    c8 = conv_block(layers.concatenate([u4, att4]), 64)

    # Classifier Head (Added to match scalar labels)
    # We keep a named conv layer for Grad-CAM access
    last_conv = layers.Conv2D(64, (3, 3), padding='same', activation='relu', name='last_conv_layer')(c8)
    gap = layers.GlobalAveragePooling2D()(last_conv)
    output = layers.Dense(1, activation='sigmoid', name='final_output')(gap)

    model = models.Model(inputs=[inputs], outputs=[output], name="Attention_UNet_Classifier")
    return model

