IMG_SIZE = (224, 224)
BATCH_SIZE = 32

def preprocess_pipeline(image):
    # Nested function to ensure it's in scope for tf.py_function
    def remove_artifacts_and_extract_roi_local(image_arg):
        # 1. Thresholding to create a mask of the breast
        gray = cv2.cvtColor(np.uint8(image_arg * 255), cv2.COLOR_RGB2GRAY)
        _, thresh = cv2.threshold(gray, 10, 255, cv2.THRESH_BINARY)

        # 2. Find the largest contour (the breast)
        contours, _ = cv2.findContours(thresh, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        if not contours:
            return image_arg

        largest_cnt = max(contours, key=cv2.contourArea)
        mask = np.zeros_like(gray)
        cv2.drawContours(mask, [largest_cnt], -1, 255, -1)

        # 3. Apply mask
        result = cv2.bitwise_and(np.uint8(image_arg * 255), np.uint8(image_arg * 255), mask=mask)

        # 4. Crop to bounding box
        x, y, w, h = cv2.boundingRect(largest_cnt)
        roi = result[y:y+h, x:x+w]

        # Resize back to target size
        roi_resized = cv2.resize(roi, (224, 224))
        return np.float32(roi_resized) / 255.0

    image_roi = remove_artifacts_and_extract_roi_local(image)
    img_uint8 = np.uint8(image_roi * 255)
    denoised = cv2.medianBlur(img_uint8, 3)
    clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8,8))
    channels = cv2.split(denoised)
    clahe_channels = [clahe.apply(c) for c in channels]
    result = cv2.merge(clahe_channels)
    return np.float32(result) / 255.0

def load_and_preprocess_image(path, label):
    img = tf.io.read_file(path)
    img = tf.image.decode_jpeg(img, channels=3)
    img = tf.image.resize(img, IMG_SIZE)
    img = img / 255.0
    [img,] = tf.py_function(preprocess_pipeline, [img], [tf.float32])
    img.set_shape((*IMG_SIZE, 3))
    return img, label

def create_dataset(df, shuffle=True):
    paths = df['full_path'].values
    labels = df['label'].values
    ds = tf.data.Dataset.from_tensor_slices((paths, labels))
    ds = ds.map(load_and_preprocess_image, num_parallel_calls=tf.data.AUTOTUNE)
    if shuffle: ds = ds.shuffle(buffer_size=1000)
    ds = ds.batch(BATCH_SIZE).prefetch(buffer_size=tf.data.AUTOTUNE)
    return ds

